/*******************************************************************************
* 函 数 名         : Lcd12864_Delay1ms
* 函数功能		   : 延时函数，延时1ms
* 输    入         : c
* 输    出         : 无
* 说    名         : 该函数是在11.0592MHZ晶振下，12分频单片机的延时。
*******************************************************************************/
/* 延时函数 */
void Lcd12864_Delay1ms(uint c)   //误差 0us
{
  uchar a,b;
	for (; c>0; c--)
	{
		 for (b=199;b>0;b--)
		 {
		  	for(a=1;a>0;a--);
		 }      
	}
    	
}

/*******************************************************************************
* 函 数 名         : LcdWriteCom
* 函数功能		   : 向LCD写入一个字节的命令
* 输    入         : com
* 输    出         : 无
*******************************************************************************/
void LcdWriteCom(uchar com)	  //写入命令
{
	LCD12864_E = 0;     //使能
	LCD12864_RS = 0;	   //选择发送命令
	LCD12864_RW = 0;	   //选择写入
	
	LCD12864_DATAPINS = com;     //放入命令
	Lcd12864_Delay1ms(1);		//等待数据稳定

	LCD12864_E = 1;	          //写入时序
	Lcd12864_Delay1ms(5);	  //保持时间
	LCD12864_E = 0;
}
/*******************************************************************************
* 函 数 名         : LcdWriteData
* 函数功能		   : 向LCD写入一个字节的数据
* 输    入         : dat
* 输    出         : 无
*******************************************************************************/		      
void LcdWriteData(uchar dat)			//写入数据
{
	LCD12864_E = 0;	//使能清零
	LCD12864_RS = 1;	//选择输入数据
	LCD12864_RW = 0;	//选择写入

	LCD12864_DATAPINS = dat; //写入数据
	Lcd12864_Delay1ms(1);

	LCD12864_E = 1;   //写入时序
	Lcd12864_Delay1ms(5);   //保持时间
	LCD12864_E = 0;
}
