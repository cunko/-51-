/* 指定位置显示字符串 注意 (1/4 0-16，"显示字符串") */
void wri_string(uchar y,uchar x,uchar a[])
{
	uchar i;
	if(y==1)//如果选择第1行
		LcdWriteCom(0x80+x);//选中地址
	if(y==2)//如果选择第2行
		LcdWriteCom(0x90+x);//选中地址
	if(y==3)//如果选择第3行
		LcdWriteCom(0x88+x);//选中地址
	if(y==4)//如果选择第4行
		LcdWriteCom(0x98+x);//选中地址
		for(i=0;a[i]!='\0';i++)
		{
			LcdWriteData(a[i]);
		}
}



/*******************************************************************************
* 函 数 名       : LcdInit()
* 函数功能		 : 初始化LCD屏
* 输    入       : 无
* 输    出       : 无
*******************************************************************************/		   
void LcdInit()						  //LCD初始化子程序
{
 	LcdWriteCom(0x34);  //开显示
	LcdWriteCom(0x30);  //开显示不显示光标
	LcdWriteCom(0x0c);  //写一个指针加1
	LcdWriteCom(0x01);  //清屏
}

/*******************************************************************************
* 函 数 名         : lcd_write_char
* 函数功能		   	:
* 输    入         :
* 输    出         : 无
*******************************************************************************/
void lcd_write_char(uchar y, uchar x, uchar dat) //列x=0~15,行y=0,1
{
	unsigned char temp_l, temp_h;
	if(y==1)//如果选择第1行
		LcdWriteCom(0x80+x);//选中地址
	if(y==2)//如果选择第2行
		LcdWriteCom(0x90+x);//选中地址
	if(y==3)//如果选择第3行
		LcdWriteCom(0x88+x);//选中地址
	if(y==4)//如果选择第4行
		LcdWriteCom(0x98+x);//选中地址
		temp_l = dat % 10;
    temp_h = dat / 10;
    LcdWriteData(temp_h + 0x30);          
    LcdWriteData(temp_l + 0x30);
}